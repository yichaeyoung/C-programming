#include <stdio.h>

int main() {
	char character = 'X';
	int inumber = 92;
	double dnumber = 20.201005;

	printf("%c\n", character);
	printf("%d\n", character);
	printf("%d\n", inumber);
	printf("%x\n", inumber);
	printf("%o\n", inumber);
	printf("%f\n", dnumber);
	printf("%e\n", dnumber);
}